public class Voluntario {
    private String nome;
    private String endereco;
    private String telefone;
    private String email;
    private String primeiraOpcao;
    private String segundaOpcao;
    private String terceiraOpcao;
    private String naoQuer;
    private String duvida;

    public Voluntario() {}

    public String getNome() { return nome; }
    public void setNome(String nome) { this.nome = nome; }

    public String getEndereco() { return endereco; }
    public void setEndereco(String endereco) { this.endereco = endereco; }

    public String getTelefone() { return telefone; }
    public void setTelefone(String telefone) { this.telefone = telefone; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public String getPrimeiraOpcao() { return primeiraOpcao; }
    public void setPrimeiraOpcao(String primeiraOpcao) { this.primeiraOpcao = primeiraOpcao; }

    public String getSegundaOpcao() { return segundaOpcao; }
    public void setSegundaOpcao(String segundaOpcao) { this.segundaOpcao = segundaOpcao; }

    public String getTerceiraOpcao() { return terceiraOpcao; }
    public void setTerceiraOpcao(String terceiraOpcao) { this.terceiraOpcao = terceiraOpcao; }

    public String getNaoQuer() { return naoQuer; }
    public void setNaoQuer(String naoQuer) { this.naoQuer = naoQuer; }

    public String getDuvida() { return duvida; }
    public void setDuvida(String duvida) { this.duvida = duvida; }
}
